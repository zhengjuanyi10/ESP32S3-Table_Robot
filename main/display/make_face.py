# Map dict key -> C variable name suffix
KEY_TO_CNAME = [
    ("happy",    "smile"),
    ("cry",      "laugh"),
    ("sad",      "sad"),
    ("wink",     "wink"),
    ("surprise", "surprise"),
]

W, H = 128, 64
pages = H // 8

def grid_to_bytes(grid):
    fb = bytearray(pages * W)
    max_lines = H // 4  # 64 rows / 4 = 16 网格行
    for gy, line in enumerate(grid[:max_lines]):
        for gx, ch in enumerate(line):
            if ch != '#':
                continue
            for py in range(4):
                row = gy * 4 + py
                page = row // 8
                bit = row % 8
                for px in range(4):
                    col = gx * 4 + px
                    fb[page * W + col] |= (1 << bit)
    return fb

output_path = os.path.join(os.path.dirname(__file__) or ".", "..", "main", "face_big_data.inc")
lines = [
    "// Auto-generated by tools/make_face.py",
    "// 32x16 grid, each char = 4x4 pixels = 128x64 full screen\n"
]

for key, cname in KEY_TO_CNAME:
    grid = FACES.get(key)
    data = grid_to_bytes(grid)
    lines.append(f"static const uint8_t s_bigface_{cname}[1024] = {{")
    for i in range(0, len(data), 16):
        chunk = data[i:i+16]
        lines.append("    " + ", ".join(f"0x{b:02X}" for b in chunk) + ",")
    lines.append("};\n")

with open(output_path, "w") as f:
    f.write("\n".join(lines))
